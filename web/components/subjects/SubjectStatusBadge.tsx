// SubjectStatusBadge.tsx
//
// Displays the current job status as a pill badge.
// Raw DB value (e.g. 'IN_PROGRESS') is normalised to title case ('In Progress')
// at render time so the DB enum value never surfaces in the UI text.
// Fixed violet colour \u2014 priority colours live in SubjectPriorityBadge.
// \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500
'use client';

interface SubjectStatusBadgeProps {
  status: string;
}

export function SubjectStatusBadge({ status }: SubjectStatusBadgeProps) {
  const label = status.replaceAll('_', ' ').replace(/\b\w/g, (c) => c.toUpperCase());

  return (
    <span className="inline-flex items-center rounded-full bg-violet-100 px-2.5 py-1 text-xs font-semibold text-violet-700">
      {label}
    </span>
  );
}
